## 未来城市的食物革命

新加坡的"30·30愿景"计划到2030年本地生产30%的食物需求。在寸土寸金的城市里，屋顶农场是这场食物革命的前沿阵地。

### Edible Garden City

这家社会企业将新加坡的闲置屋顶改造成了城市农场。从金沙酒店附近的屋顶到组屋区的社区花园，他们种植了超过200种可食用植物。

### 体验内容

**上午** 跟随城市农夫学习垂直种植技术：水培系统、鱼菜共生、堆肥制作。亲手种下一株香草或一棵生菜。

**中午** 在屋顶餐厅享用"从屋顶到餐桌"的午餐。所有食材都在100米范围内种植，碳足迹接近于零。

**下午** 参与社区农场的收获活动，学习如何识别成熟蔬菜，用剪刀收割，直接装进回收袋。你会带走自己收获的一袋新鲜蔬菜。

### 都市农业的意义

新加坡目前90%以上的食物依赖进口。屋顶农场不仅提供新鲜食材，还能降低城市热岛效应，减少雨水径流，并为社区创造绿色就业机会。

