## "餐桌上的钻石"狩猎之旅

白松露是世界上单价最贵的食材，每公斤可达数千欧元。在意大利皮埃蒙特的阿尔巴地区，每年10月至12月是白松露季节，你可以跟随专业的松露猎人进入秘密森林。

### 松露猎人

松露猎人是这个传统最神秘的守护者。他们通常在凌晨4点出发，带着训练有素的猎犬进入森林。每个猎人都有自己的"秘密地图"，代代相传，从不向外人透露。

### 狩猎流程

1. 黎明前与松露猎人会合，驾驶越野车进入山区
2. 在雾气弥漫的森林中徒步，观察猎犬如何通过气味定位松露
3. 猎犬开始挖掘时，猎人会快速接手，用特制的小铲小心翼翼地取出松露
4. 凑近鼻子——那种独特的香气，混合着泥土、大蒜、麝香和森林的气息

### 松露鉴赏

狩猎结束后，回到当地农庄，松露猎人会为你准备一顿白松露主题的盛宴：手工意面上刨下薄薄的白松露片，每一口都是奢华的极致。

